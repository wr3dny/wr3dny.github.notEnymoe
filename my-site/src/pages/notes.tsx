<p>
  JAMstack ( JavaScript, API, Markup ) to architektura stosaowana w tworzeniu
  nowoczesnych stron internetowych i aplikacji. W JAMstack, cała strona
  internetowa jest generowana i dostarczana jako zestaw statycznych plików HTML,
  CSS i JavaScript. Wszystkie interaktywne elementy są dodawane za pomocą
  JavaScriptu, a nie serwera. Wszystkie dane są pobierane za pomocą API.
</p>;
